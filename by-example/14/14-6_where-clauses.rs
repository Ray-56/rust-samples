use std::fmt::Debug;

trait PrintInOption {
    fn print_in_option(self);
}

// 这里需要一个`where`从句，否则就要表达成`T:Debug`
impl<T> PrintInOption for T where
    Option<T>: Debug {
    // 我们要将`Option<T>: Debug`作为约束，因为那是要打印的内容
    // 否则我们会给出错误的约束
    fn print_in_option(self) {
        println!("{:?}", Some(self));
    }
}


fn main() {
    let vec = vec![1, 2, 3];

    vec.print_in_option();
}